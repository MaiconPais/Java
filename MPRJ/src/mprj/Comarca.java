package mprj;

/**
 *
 * @author thiago.carvalho
 */
public class Comarca {
    int idComarca ;
    String craai;
    String orgaoExecucao;
    String endereco;
    String telefone;
    String materia;

    public int getIdComarca() {
        return idComarca;
    }

    public String getCraai() {
        return craai;
    }

    public String getOrgaoExecucao() {
        return orgaoExecucao;
    }

    public String getEndereco() {
        return endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public String getMateria() {
        return materia;
    }

    public void setIdComarca(int idComarca) {
        this.idComarca = idComarca;
    }

    public void setCraai(String craai) {
        this.craai = craai;
    }

    public void setOrgaoExecucao(String orgaoExecucao) {
        this.orgaoExecucao = orgaoExecucao;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public void setMateria(String materia) {
        this.materia = materia;
    }
    
    
    
}
